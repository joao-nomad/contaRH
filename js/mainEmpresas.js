setInterval(function(){
    var $header = $("#top");

    if ($header.hasClass("bg4") == true)
    {
        $header.removeClass("bg4");
        $header.fadeOut("fast");
        $header.fadeIn("fast");
       $header.addClass("bg5");
    }
    else if ($header.hasClass("bg5") == true)
    {
        $header.removeClass("bg5");
        $header.fadeOut("fast");
        $header.fadeIn("fast");
        $header.addClass("bg4");
    }
}, 4000);


